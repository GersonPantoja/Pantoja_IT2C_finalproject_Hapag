				tell if it’s already set and tender.
			</li><br/>
			<li>
				You can already add your vegetables. Start by adding the sitaw, kangkong 
				stalks and talong into the pot <br/>and cook until these turn dark green. 
				If you decide to use labanos and okra, you can also add those at this <br/> 
				point. This should take roughly around 2-3 minutes.
			</li><br/>
			<li>
				Add in your siling sigang, kangkong leaves and the Knorr Sinigang sa 
				Sampaloc Mix. Stir and let it <br/>simmer. It should be done in about 2-3 
				minutes.
			</li><br/>
			<li>
				All done! Transfer your Sinigang to a serving bowl. This is a happy treat 
				that warms, soothes and satisfies <br/>everyone in the family! Remember to prepare 
				extra servings of rice to enjoy the extra asim kilig taste of <br/>this Filipino 
				classic soup. Enjoy!
			</li><br/>
