			</li> <br/>
			<li>
				Add coconut milk, water, butter, and eggs. Stir until smooth and
				well-blended.
			</li> <br/>
			<li>
				Cover the batter and refrigerate overnight.
			</li><br/>
			<li>
				Rinse banana leaves under warm water and wipe off any dirt or grit.
