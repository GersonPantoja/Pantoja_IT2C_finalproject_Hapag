		<h2 style="font-family: Georgia;"><b>Tapsilog</b></h2>
		<h3><i>Ingredients:</i></h3>
		<?php
			echo "1 lb. beef sirloin sliced thinly", "<br/>";
			echo "3 pieces eggs", "<br/>";
			echo "6 tablespoons cooking oil";
		?>
		<h3><i>Tapa Marinade:</i></h3>
		<?php

			echo "3 tablespoons soy sauce (Knorr Liquid Seasoning optional)", "<br/>";
			echo "6 cloves crushed garlic", "<br/>";
			echo "¾ cups pineapple juice", "<br/>";
			echo "2 tablespoons brown sugar", "<br/>";
			echo "¼ teaspoon ground white pepper";
		?>
		<h3><i>Sinangag:</i></h3>
		<?php

			echo "5 cloves garlic crushed", "<br/>";
			echo "5 cups leftover rice", "<br/>";
			echo "1 teaspoon salt";
		?>
		<h3><i> Directions: </i></h3>
		<ol>
			<li>
				Prepare the tapa by placing the beef in a large bowl. Combine with all the tapa marinade ingredients. <br/> Mix well and cover the bowl.Place inside the fridge and marinate overnight. 
			</li> <br/>
			<li>
				Cook the garlic fried rice (sinangag na kanin) by heating 3 tablespoons cooking oil in a pan. Add crushed garlic. <br/>Cook until garlic turns light brown. Add the leftover rice. Stir-fry for 3 minutes.
			</li> <br/>
			<li>
				Season with salt. Continue to stir-fry for 3 to 5 minutes. Set aside.
			</li> <br/>
			<li>
				Start to cook the tapa. Heat a pan and pour the marinated beef into it, including the marinade. Add ¾ cups water. <br/>Let the mixture boil. Cover the pan and continue to cook until the liquid reduces to half. Add 3 tablespoons cooking <br/>oil into the mixture. Continue to cook until the liquid completely evaporates. Fry the beef tapa in remaining oil until <br/>medium brown. Set aside.
			</li> <br/>
			<li>
				Fry the egg by pouring 1 tablespoon oil on a pan. Crack a piece of egg and sprinkle enough salt on top. Cook for <br/>30 seconds.Pour 2 tablespoons water on the side of the pan. Cover and let the water boil. Continue to cook until the<br/> egg yolks gets completely cooked by the steam.
			</li> <br/>
			<li>
				Arrange the beef tapa, sinangag, and fried egg on a large plate to form Tapsilog. Serve with vinegar as dipping <br/>sauce for tapa.
			</li><br/>
		</ol>
