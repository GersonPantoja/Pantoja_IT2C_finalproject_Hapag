				seconds or until just softened.<br/> Line the pans, making sure the
				leaves are intact with no rips.
			</li><br/>
			<li>
				Pour the batter into the prepared pans up to 3/4 full. Arrange sliced
				salted eggs and cream cheese <br/>slices on top.
			</li><br/>
			<li>
				Arrange the filled tin molds in a single layer on a baking sheet and 
				place in the oven on the lower <br/> rack. Bake in a 375 F oven for 
				about 20 to 25 minutes or until<br/> a toothpick inserted in the middle
				comes out clean.
			</li><br/>
			<li>
				Transfer the baking sheet on the top rack and continue to cook for
				about 3 to 5 minutes or until<br/> lightly golden.
			</li><br/>
			<li>
				Remove from heat. Spread margarine or butter and sprinkle sugar on top.
				Garnish with grated coconut. <br/>Serve hot.
			</li><br/>
