				<h3><i>Ingredients: </i></h3>
					<?php
						echo "1 large carrot, peeled and sliced on a bias into 1/4-inch thick", "<br/>";
						echo "1/2 broccoli, cut into florets", "<br/>";
						echo "1/2 cauliflower, cut into florets", "<br/>";
						echo "1/2 cabbage, cut into 1-inch thick strips", "<br/>";
						echo "1/2 red bell pepper, seeded and cut into strips", "<br/>";
						echo "1/2 bell pepper, seeded and cut into strips", "<br/>";
						echo "3 tablespoons canola oil", "<br/>";
						echo "1/4 pound chicken liver, cut thin strips", "<br/>";
						echo "1 onion, peeled and sliced", "<br/>";
						echo "2 cloves garlic, peeled and minced", "<br/>";
						echo "1/2 pound chicken thigh fillets, cut into thin strips", "<br/>";
						echo "2 cups poaching liquid (reserved from parboiling the vegetables)", "<br/>";
						echo "2 tablespoons oyster sauce", "<br/>";
						echo "5 pieces baby corn, halved crosswise", "<br/>";
						echo "6 hardboiled quail eggs, peeled", "<br/>";
						echo "1/4 cup water", "<br/>";
						echo "1 tablespoon corn starch", "<br/>";
						echo "salt and pepper to taste", "<br/>";
					?>

					<h3 id="direction"><i> Directions: </i></h3>
					<ol>
			<li>
				Fill a bowl halfway with ice and enough water to cover ice. Add 1/2 teaspoon 
				salt for each quart of water. Set aside.
			</li><br/>
			<li>
				In a saucepan over medium heat, bring 3 cups of salted water to a boil. Add 
				carrots and cook for about 1 minute or until half done. With a slotted spoon, 
				remove from pan and plunge into bowl of ice bath. 
			</li><br/>
			<li>
				Add broccoli and cauliflower to the boiling water and cook for about 2 to 3 
				minutes or until half-done. With a slotted spoon, remove from pan and plunge 
				into the ice bath.
			</li><br/>
			<li>
				Add cabbage to the boiling water and cook for about 30 seconds or until half-done. 
				With a slotted spoon, remove from pan and plunge into the ice bath.
			</li><br/>
			<li>
				Add peppers to the boiling water and cook for about 30 seconds or until half-done. 
				With a slotted spoon, remove from pan and plunge into the ice bath.
			</li><br/>
			<li>
				Reserve 2 cups of the poaching liquid (the one used to blanch vegetables).
			</li><br/>
			<li>
				Drain vegetables from the ice bath when they are cold.
			</li><br/>
			<li>
				In a wok or wide skillet over medium heat, heat about 2 tablespoons of the oil.
			</li><br/>
			<li>
				Add chicken liver in a single layer and fry until lightly browned on all sides 
				but not fully cooked. Remove from pan and keep warm.
			</li><br/>
			<li>
				Discard excess oil and wipe down the pan. Add the remaining 1 tablespoon of 
				oil and heat.
			</li><br/>
			<li>
				Add onions and garlic and cook until softened.
			</li><br/>
			<li>
				Add chicken and cook, stirring regularly, until color changes.
			</li><br/>
			<li>
				In a bowl, combine the reserved poaching liquid and oyster sauce. Add to the 
				pan and bring to a boil. Continue to cook, skimming scum that may float on top, 
				for about 4 to 5 minutes or until chicken is cooked through. 
			</li><br/>
			<li>
				Add the liver and cook for 1 to 2 minutes.
			</li><br/>
			<li>
				Add parboiled vegetables, baby corn, and quail eggs, stirring gently to combine, 
				and cook for about 3 to 5 minutes.
			</li><br/>
			<li>
				In a bowl, combine 1/4 cup of cold water and corn starch and stir until corn starch 
				is dissolved. Add mixture to the pan, stirring gently. Cook for about 1 to 2 minutes 
				or until sauce is thickened.
			</li><br/>
			<li>
				Season with salt and pepper to taste. Serve hot.
			</li><br/>
		</ol>
					
