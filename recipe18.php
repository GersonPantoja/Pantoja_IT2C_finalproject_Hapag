					<h3 id="direction"><i> Directions: </i></h3>
					<ol>
			<li>
				Prepare the saba banana by peeling and cutting into even slices about three 
				millimeters thick. Three ripe <br/>bananas will give you about 12 pieces total. 
				Prepare the jackfruit by slicing into strips.
			</li><br/>
			<li>
				Prep your workstation by putting the dark brown sugar in a shallow plate, 
				pre-peel the individual spring <br/>roll wrappers and cover them with a 
				damp cloth, and place a water bowl next to your surface.
			</li><br/>
			<li>
				Basic egg-roll technique: Place a spring roll wrapper in a diamond shape in 
				front of you (one corner is at <br/>the top of your workstation, while another corner 
				is facing the bottom). Take one banana slice and coat it <br/>lightly with the brown 
				sugar, then place it about two inches from the bottom center. Take two to three 
				strips <br/>of jackfruit and place it on top of the banana slice. Lift the bottom corner 
				and cover the filling. Take the left <br/>and right corners and fold them towards the 
				center. Finish rolling the entire turon upwards until you <br/>have about one inch left. 
				Use your fingers to brush the top corner with water and complete the roll to seal. 
				<br/>Repeat with the rest of the turon.
			</li><br/>
			<li>
				One-roll technique: Place a spring roll wrapper in a diamond shape in front of you. 
				Take one banana slice <br/>and coat it lightly with the brown sugar, then place it about 
				two inches from the bottom center. Take two to <br/>three strips of jackfruit and place 
				it on top of the banana slice. Lift the bottom corner and cover the filling. <br/>Continue 
				to roll upwards until you have one inch left of space. Use your fingers to brush 
				the top corner <br/>with water and complete the roll to seal. Repeat with the rest of the 
				turon.
			</li><br/>
			<li>
				In a pan wide enough to fit the width of your turon (this is important especially if 
				you used the one-roll technique),<br/> fill enough vegetable oil (or other neutral oil) 
				until it’s about two inches high. Heat your oil to 350 °Fs.
			</li><br/>
			<li>
				When the oil is hot enough, carefully lower the turon into the oil. Deep fry for about 
				one minute before turning <br/>over and cooking for another minute or until the turon 
				is nice and golden brown. Remove the turon and place <br/>them onto a wire rack. Repeat 
				with the rest of the turon.
			</li><br/>
		</ol>
		<h3><i> Caramel Coating: </i></h3>
		<ol>
			<li>
				In a new saucepan over medium-low heat, add the white sugar and watch carefully until it 
				begins to melt.<br/> Use a spatula to move the melted sugar around and continue to cook for 
				about one to two minutes or until <br/>the sugar is light golden brown. Remove the pan from 
				the heat immediately.
			</li><br/>
			<li>
				Use a spoon to drizzle the caramel over the fried turon or use tongs to dip and roll the 
				turon right into the <br/>caramel pan. Be careful when dipping because the caramel can be very 
				hot.
			</li><br/>
			<li>
				Let the turon rest for about one minute. The caramel should harden and it should be ready to serve!
			</li><br/>
		</ol>
					
